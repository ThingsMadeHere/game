Vector3 MarchingCubes::InterpolateVertex(Vector3 p1, Vector3 p2, float d1, float d2) {
    float t = d1 / (d1 - d2);
    if (t < 0) t = 0;
    if (t > 1) t = 1;
    return {p1.x + t * (p2.x - p1.x), p1.y + t * (p2.y - p1.y), p1.z + t * (p2.z - p1.z)};
}

float MarchingCubes::GetDensityWithNeighbors(const Chunk& chunk, const std::unordered_map<ChunkKey, Chunk>& allChunks, int x, int y, int z) {
    // First try to get density from current chunk
    if (x >= 0 && x < CHUNK_SIZE && y >= 0 && y < CHUNK_HEIGHT && z >= 0 && z < CHUNK_SIZE) {
        return chunk.GetDensity(x, y, z);
    }
    
    // If outside current chunk, try to get from neighbor chunk
    int neighborCx = chunk.cx;
    int neighborCy = chunk.cy;
    int neighborCz = chunk.cz;
    
    if (x < 0) { neighborCx--; x += CHUNK_SIZE; }
    else if (x >= CHUNK_SIZE) { neighborCx++; x -= CHUNK_SIZE; }
    
    if (y < 0) { neighborCy--; y += CHUNK_HEIGHT; }
    else if (y >= CHUNK_HEIGHT) { neighborCy++; y -= CHUNK_HEIGHT; }
    
    if (z < 0) { neighborCz--; z += CHUNK_SIZE; }
    else if (z >= CHUNK_SIZE) { neighborCz++; z -= CHUNK_SIZE; }
    
    ChunkKey neighborKey = {neighborCx, neighborCy, neighborCz};
    auto it = allChunks.find(neighborKey);
    if (it != allChunks.end()) {
        return it->second.GetDensity(x, y, z);
    }
    
    // If neighbor doesn't exist, use the EXACT same terrain generation logic
    // This ensures perfect boundary consistency
    
    // Convert to world coordinates (same as terrain generation)
    float worldX = (float)(neighborCx * CHUNK_SIZE + x) * VOXEL_SIZE;
    float worldY = (float)(neighborCy * CHUNK_HEIGHT + y) * VOXEL_SIZE;
    float worldZ = (float)(neighborCz * CHUNK_SIZE + z) * VOXEL_SIZE;
    
    // Use the EXACT same ground height calculation
    float groundHeight = 8.0f + SmoothNoise3D(worldX * 0.02f, 0, worldZ * 0.02f) * 6.0f;
    
    // DEBUG: Force flat ground at y=0 for testing visibility
    groundHeight = 2.0f;
    
    // Use the EXACT same density logic
    float density = 0.0f;
    
    if (worldY < groundHeight) {
        density = 1.0f; // Solid ground
        
        // Add caves with EXACT same logic
        if (worldY < groundHeight - 2.0f) {
            float caveNoise = SmoothNoise3D(worldX * 0.05f, worldY * 0.05f, worldZ * 0.05f);
            if (caveNoise > 0.6f) {
                density = -1.0f; // Cave
            }
        }
    } else {
        density = -1.0f; // Air
    }
    
    return density;
}

Mesh MarchingCubes::GenerateMesh(const Chunk& chunk) {
    // For backward compatibility, call the neighbor-aware version with empty chunks map
    std::unordered_map<ChunkKey, Chunk> emptyChunks;
    return GenerateMesh(chunk, emptyChunks);
}

Mesh MarchingCubes::GenerateMesh(const Chunk& chunk, const std::unordered_map<ChunkKey, Chunk>& allChunks) {
    std::vector<float> vertices;
    std::vector<float> normals;
    
    int cubesProcessed = 0;
    int trianglesGenerated = 0;
    
    // Marching Cubes implementation with proper boundary handling
    for (int x = 0; x < CHUNK_SIZE - 1; x++) {
        for (int y = 0; y < CHUNK_HEIGHT - 1; y++) {
            for (int z = 0; z < CHUNK_SIZE - 1; z++) {
                cubesProcessed++;
                
                // Get densities at cube corners with thresholding
                float densities[8];
                float threshold = 0.25f; // Slightly lower threshold to fill gaps
                
                densities[0] = GetDensityWithNeighbors(chunk, allChunks, x, y, z) > threshold ? 1.0f : -1.0f;
                densities[1] = GetDensityWithNeighbors(chunk, allChunks, x + 1, y, z) > threshold ? 1.0f : -1.0f;
                densities[2] = GetDensityWithNeighbors(chunk, allChunks, x + 1, y, z + 1) > threshold ? 1.0f : -1.0f;
                densities[3] = GetDensityWithNeighbors(chunk, allChunks, x, y, z + 1) > threshold ? 1.0f : -1.0f;
                densities[4] = GetDensityWithNeighbors(chunk, allChunks, x, y + 1, z) > threshold ? 1.0f : -1.0f;
                densities[5] = GetDensityWithNeighbors(chunk, allChunks, x + 1, y + 1, z) > threshold ? 1.0f : -1.0f;
                densities[6] = GetDensityWithNeighbors(chunk, allChunks, x + 1, y + 1, z + 1) > threshold ? 1.0f : -1.0f;
                densities[7] = GetDensityWithNeighbors(chunk, allChunks, x, y + 1, z + 1) > threshold ? 1.0f : -1.0f;
                
                // Calculate cube index
                int cubeIndex = 0;
                for (int i = 0; i < 8; i++) {
                    if (densities[i] > 0.0f) {
                        cubeIndex |= (1 << i);
                    }
                }
                
                // Skip if completely inside or outside
                if (cubeIndex == 0 || cubeIndex == 255) {
                    continue;
                }
                
                // Get triangle configuration for this cube
                const int* triConfig = triTable[cubeIndex];
                
                // Generate triangles for this cube
                for (int i = 0; i < 16 && triConfig[i] != -1; i += 3) {
                    Vector3 edgeVerts[12];
                    
                    // Calculate interpolated vertices for each edge
                    // Edge 0: v0 to v1
                    if ((edgeTable[cubeIndex] & 1) != 0) {
                        Vector3 p1 = {(float)x, (float)y, (float)z};
                        Vector3 p2 = {(float)(x + 1), (float)y, (float)z};
                        edgeVerts[0] = InterpolateVertex(p1, p2, densities[0], densities[1]);
                    }
                    // Edge 1: v1 to v2
                    if ((edgeTable[cubeIndex] & 2) != 0) {
                        Vector3 p1 = {(float)(x + 1), (float)y, (float)z};
                        Vector3 p2 = {(float)(x + 1), (float)y, (float)(z + 1)};
                        edgeVerts[1] = InterpolateVertex(p1, p2, densities[1], densities[2]);
                    }
                    // Edge 2: v2 to v3
                    if ((edgeTable[cubeIndex] & 4) != 0) {
                        Vector3 p1 = {(float)(x + 1), (float)y, (float)(z + 1)};
                        Vector3 p2 = {(float)x, (float)y, (float)(z + 1)};
                        edgeVerts[2] = InterpolateVertex(p1, p2, densities[2], densities[3]);
                    }
                    // Edge 3: v3 to v0
                    if ((edgeTable[cubeIndex] & 8) != 0) {
                        Vector3 p1 = {(float)x, (float)y, (float)(z + 1)};
                        Vector3 p2 = {(float)x, (float)y, (float)z};
                        edgeVerts[3] = InterpolateVertex(p1, p2, densities[3], densities[0]);
                    }
                    // Edge 4: v4 to v5
                    if ((edgeTable[cubeIndex] & 16) != 0) {
                        Vector3 p1 = {(float)x, (float)(y + 1), (float)z};
                        Vector3 p2 = {(float)(x + 1), (float)(y + 1), (float)z};
                        edgeVerts[4] = InterpolateVertex(p1, p2, densities[4], densities[5]);
                    }
                    // Edge 5: v5 to v6
                    if ((edgeTable[cubeIndex] & 32) != 0) {
                        Vector3 p1 = {(float)(x + 1), (float)(y + 1), (float)z};
                        Vector3 p2 = {(float)(x + 1), (float)(y + 1), (float)(z + 1)};
                        edgeVerts[5] = InterpolateVertex(p1, p2, densities[5], densities[6]);
                    }
                    // Edge 6: v6 to v7
                    if ((edgeTable[cubeIndex] & 64) != 0) {
                        Vector3 p1 = {(float)(x + 1), (float)(y + 1), (float)(z + 1)};
                        Vector3 p2 = {(float)x, (float)(y + 1), (float)(z + 1)};
                        edgeVerts[6] = InterpolateVertex(p1, p2, densities[6], densities[7]);
                    }
                    // Edge 7: v7 to v4
                    if ((edgeTable[cubeIndex] & 128) != 0) {
                        Vector3 p1 = {(float)x, (float)(y + 1), (float)(z + 1)};
                        Vector3 p2 = {(float)x, (float)(y + 1), (float)z};
                        edgeVerts[7] = InterpolateVertex(p1, p2, densities[7], densities[4]);
                    }
                    // Edge 8: v0 to v4
                    if ((edgeTable[cubeIndex] & 256) != 0) {
                        Vector3 p1 = {(float)x, (float)y, (float)z};
                        Vector3 p2 = {(float)x, (float)(y + 1), (float)z};
                        edgeVerts[8] = InterpolateVertex(p1, p2, densities[0], densities[4]);
                    }
                    // Edge 9: v1 to v5
                    if ((edgeTable[cubeIndex] & 512) != 0) {
                        Vector3 p1 = {(float)(x + 1), (float)y, (float)z};
                        Vector3 p2 = {(float)(x + 1), (float)(y + 1), (float)z};
                        edgeVerts[9] = InterpolateVertex(p1, p2, densities[1], densities[5]);
                    }
                    // Edge 10: v2 to v6
                    if ((edgeTable[cubeIndex] & 1024) != 0) {
                        Vector3 p1 = {(float)(x + 1), (float)y, (float)(z + 1)};
                        Vector3 p2 = {(float)(x + 1), (float)(y + 1), (float)(z + 1)};
                        edgeVerts[10] = InterpolateVertex(p1, p2, densities[2], densities[6]);
                    }
                    // Edge 11: v3 to v7
                    if ((edgeTable[cubeIndex] & 2048) != 0) {
                        Vector3 p1 = {(float)x, (float)y, (float)(z + 1)};
                        Vector3 p2 = {(float)x, (float)(y + 1), (float)(z + 1)};
                        edgeVerts[11] = InterpolateVertex(p1, p2, densities[3], densities[7]);
                    }
                    
                    // Add triangle vertices to mesh
                    for (int j = 0; j < 3; j++) {
                        int edgeIndex = triConfig[i + j];
                        Vector3 v = edgeVerts[edgeIndex];
                        
                        // Scale by voxel size
                        v.x *= VOXEL_SIZE;
                        v.y *= VOXEL_SIZE;
                        v.z *= VOXEL_SIZE;
                        
                        // Add vertex
                        vertices.push_back(v.x);
                        vertices.push_back(v.y);
                        vertices.push_back(v.z);
                        
                        // Calculate proper normal for the triangle
                        Vector3 v1 = edgeVerts[triConfig[i]];
                        Vector3 v2 = edgeVerts[triConfig[i+1]];
                        Vector3 v3 = edgeVerts[triConfig[i+2]];
                        
                        // Calculate normal using cross product
                        Vector3 edge1 = {v2.x - v1.x, v2.y - v1.y, v2.z - v1.z};
                        Vector3 edge2 = {v3.x - v1.x, v3.y - v1.y, v3.z - v1.z};
                        
                        // Cross product for normal
                        Vector3 normal = {
                            edge1.y * edge2.z - edge1.z * edge2.y,
                            edge1.z * edge2.x - edge1.x * edge2.z,
                            edge1.x * edge2.y - edge1.y * edge2.x
                        };
                        
                        // Normalize the normal vector
                        float length = sqrtf(normal.x * normal.x + normal.y * normal.y + normal.z * normal.z);
                        if (length > 0.001f) {
                            normal.x /= length;
                            normal.y /= length;
                            normal.z /= length;
                        } else {
                            normal = {0.0f, 1.0f, 0.0f}; // Default up
                        }
                        
                        normals.push_back(normal.x);
                        normals.push_back(normal.y);
                        normals.push_back(normal.z);
                    }
                    
                    trianglesGenerated++;
                }
            }
        }
    }
    
    if (vertices.empty()) {
        fprintf(stderr,"Empty mesh generated for chunk (%d,%d,%d) - returning empty mesh\n", 
               chunk.cx, chunk.cy, chunk.cz);
        return {0};
    }
    
    fprintf(stderr,"Marching Cubes: %d cubes, %d triangles, %d vertices\n", 
           cubesProcessed, trianglesGenerated, (int)vertices.size() / 3);

    // Create proper mesh with correct structure
    Mesh resultMesh = {0};
    resultMesh.vertexCount = vertices.size() / 3;
    resultMesh.triangleCount = vertices.size() / 9; // 3 vertices per triangle
            
    // Allocate and copy vertex data
    resultMesh.vertices = (float*)malloc(vertices.size() * sizeof(float));
    resultMesh.normals = (float*)malloc(normals.size() * sizeof(float));
    resultMesh.indices = (unsigned short*)malloc(resultMesh.triangleCount * 3 * sizeof(unsigned short));
    
    memcpy(resultMesh.vertices, vertices.data(), vertices.size() * sizeof(float));
    memcpy(resultMesh.normals, normals.data(), normals.size() * sizeof(float));
    
    // Generate simple indices (0, 1, 2, 3, 4, 5, ...)
    for (int i = 0; i < resultMesh.triangleCount * 3; i++) {
        resultMesh.indices[i] = i;
    }
    
    return resultMesh;
}
